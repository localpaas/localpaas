package fileuc

import (
	"context"

	"github.com/localpaas/localpaas/localpaas_app/apperrors"
	"github.com/localpaas/localpaas/localpaas_app/basedto"
	"github.com/localpaas/localpaas/localpaas_app/usecase/settings"
	"github.com/localpaas/localpaas/localpaas_app/usecase/settings/fileuc/filedto"
)

func (uc *UC) UpdateFileMeta(
	ctx context.Context,
	auth *basedto.Auth,
	req *filedto.UpdateFileMetaReq,
) (*filedto.UpdateFileMetaResp, error) {
	req.Type = currentSettingType
	_, err := uc.UpdateSettingMeta(ctx, &req.UpdateSettingMetaReq, &settings.UpdateSettingMetaData{})
	if err != nil {
		return nil, apperrors.Wrap(err)
	}

	return &filedto.UpdateFileMetaResp{}, nil
}
