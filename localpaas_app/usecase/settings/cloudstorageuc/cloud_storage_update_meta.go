package cloudstorageuc

import (
	"context"

	"github.com/localpaas/localpaas/localpaas_app/apperrors"
	"github.com/localpaas/localpaas/localpaas_app/basedto"
	"github.com/localpaas/localpaas/localpaas_app/usecase/settings"
	"github.com/localpaas/localpaas/localpaas_app/usecase/settings/cloudstorageuc/cloudstoragedto"
)

func (uc *CloudStorageUC) UpdateCloudStorageMeta(
	ctx context.Context,
	auth *basedto.Auth,
	req *cloudstoragedto.UpdateCloudStorageMetaReq,
) (*cloudstoragedto.UpdateCloudStorageMetaResp, error) {
	req.Type = currentSettingType
	_, err := uc.UpdateSettingMeta(ctx, &req.UpdateSettingMetaReq, &settings.UpdateSettingMetaData{})
	if err != nil {
		return nil, apperrors.Wrap(err)
	}

	return &cloudstoragedto.UpdateCloudStorageMetaResp{}, nil
}
